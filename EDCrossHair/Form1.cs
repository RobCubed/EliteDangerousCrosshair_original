        private void RunPaint()
        {
            Process[] processes = Process.GetProcessesByName("EliteDangerous32");
            Process lol = processes[0];
            IntPtr ptr = lol.MainWindowHandle;
            Rect NotepadRect = new Rect();
            GetWindowRect(ptr, ref NotepadRect);
            RECT clientRect = GetClientRect(ptr);

            Console.WriteLine(clientRect.Height + " " + clientRect.Width);
            int x;
            int y;
            bool coordinatesFound;
            int newPointX;
            int newPointY;

            while (true)
            {
                Point point;
                x = clientRect.Width / 2;
                y = clientRect.Height / 2;
                coordinatesFound = false;

                point = new Point();

                coordinatesFound = ClientToScreen(ptr, ref point);

                newPointX = point.X + x;
                newPointY = point.Y + y;

                if (coordinatesFound == true && (newPointX != bufferedPanel.xDraw || newPointY != bufferedPanel.yDraw))
                {
                    Console.WriteLine("Moved: " + (point.X + x) + " " + (point.Y + y));
                    bufferedPanel.xDraw = newPointX;
                    bufferedPanel.yDraw = newPointY;
                }

            }
        }

